    @property
    def is_time_reversal(self)-> bool:
        '''
        Time-reversal-symmetric or not.
        '''
        return "1'" in self.ROTATIONS[self.hermann_mauguin]
    @property
    def is_pt_symmetric(self)-> bool:
        '''
        PT-symmetric or not.
        '''
        return "-1'" in self.ROTATIONS[self.hermann_mauguin]
    def check_closure(self)-> bool:
        '''
        Check if implemented point group is closed.

        Returns:
            bool: True for closed.
        '''
        symmmats = [(symmop.matrix_prim, symmop.is_time_reversal)
                    for symmop in self.symmops]
        for (symmmat_i, tr_i) in symmmats:
            for (symmmat_j, tr_j) in symmmats:
                symmmat_prod = symmmat_i * symmmat_j
                tr_prod = tr_i ^ tr_j
                if not (symmmat_prod, tr_prod) in symmmats:
                    return False
        return True

