    '''
    Method to write LaTeX file of all point groups.

    Args:
        filename (Path):
    '''
    symmetry.write_preamble(filename = filename)
    for (i, hermann_mauguin) in enumerate(list(MagneticPointGroup.ROTATIONS.keys())):
        mpg = MagneticPointGroup(hermann_mauguin = hermann_mauguin)
        mpg.write_latex(filename = filename)
    symmetry.write_postamble(filename = filename)

